// Function expression - divide
const divide = function(a, b) {
    return a / b;
  };
  
  // Arrow function - square
  const square = (num) => {
    return num * num;
  };
  
  // Arrow function - add
  const add = (a, b) => {
    return a + b;
  };
  
  // Export functions if needed (depending on your setup)
  module.exports = {
    divide,
    square,
    add
  };
  